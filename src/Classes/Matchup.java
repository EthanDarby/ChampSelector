package Classes;

public class Matchup {

	public String name;
	public long champId;
	public long wins;
	public long losses;
	public long totalMatchups;
	public double killsWhenWin;
	public double deathsWhenWin;
	public double killsWhenLose;
	public double deathsWhenLose;
	public double assistsWhenWin;
	public double
	assistsWhenLose;
	
	public Matchup(){
		
		
		this.name = "none";
		this.champId = 0;
		this.wins = 0;
		this.losses = 0;
		this.totalMatchups = 0;
		this.killsWhenWin = 0;
		this.deathsWhenWin = 0;
		this.killsWhenLose = 0;
		this.deathsWhenLose = 0;
		this.assistsWhenWin = 0;
		this.assistsWhenWin = 0;
		
	}
	
}
